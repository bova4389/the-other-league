h2h = defaultdict(lambda: defaultdict(lambda: [0,0]))
games_count = 0

for year, weeks in seasons.items():
    for wk_idx, (pts_dict, mid_dict) in enumerate(weeks):
        groups = defaultdict(list)
        for rid, pts in pts_dict.items():
            mid = mid_dict[rid]
            groups[mid].append((rid, pts))
        for mid, pair in groups.items():
            if len(pair) != 2:
                print(f"WARN year={year} wk={wk_idx+1} mid={mid}: {pair}")
                continue
            (a, pa), (b, pb) = pair
            games_count += 1
            if pa > pb:
                h2h[a][b][0] += 1
            elif pb > pa:
                h2h[b][a][0] += 1

print(f"Total games processed: {games_count}  (expect 252 = 6 games/wk * 14 wks * 3 yrs)\n")

# Print full H2H matrix sorted by total wins desc
rosters = list(range(1, 13))
totals = {}
for r in rosters:
    w = sum(h2h[r][o][0] for o in rosters if o != r)
    l = sum(h2h[o][r][0] for o in rosters if o != r)
    totals[r] = (w, l)

sorted_r = sorted(rosters, key=lambda r: totals[r][0], reverse=True)

print("=" * 60)
print("ALL-TIME H2H RECORDS (Regular Season Weeks 1-14, 2023-2025)")
print("=" * 60)
for r in sorted_r:
    w, l = totals[r]
    print(f"\n{names[r]} ({w}-{l} all-time)")
    opps = [(o, h2h[r][o][0], h2h[o][r][0]) for o in rosters if o != r]
    opps.sort(key=lambda x: x[1]-x[2], reverse=True)
    for o, ow, ol in opps:
        bar = "W" if ow > ol else ("L" if ol > ow else "T")
        print(f"  vs {names[o]:<20} {ow}-{ol}  [{bar}]")
